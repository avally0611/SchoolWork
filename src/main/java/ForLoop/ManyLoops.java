package ForLoop;

/**
 *
 * @author aaminahv
 */
public class ManyLoops {
    public static void main(String[] args) {
        
        
        for (int i = 10; i >=1; i--) {
            System.out.println(i);
            
            
        }
        
        System.out.println();
        
        for (int num = 25; num <=30; num++) {
            System.out.println(num);
            
            
        }
    }
    
}
